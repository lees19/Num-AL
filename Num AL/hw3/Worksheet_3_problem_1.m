format long e;
f=inline('1+10*x+45*x^2+120*x^3+210*x^4+252*x^5+210*x^6+120*x^7+45*x^8+10*x^9+x^10');
g=inline('(x+1)^10');
currentPrecision = digits(4);
xd=[];
xd(1)=-1.010;
o1=[];
o2=[];
d=0.0001;
results=fopen('resultspolynomial_error.txt','w');
fprintf(results,'%20s %20s %20s\n','x','P1','P2');
for k=1:201;
    xi=vpa(xd(k));
    %display(xi);
    o1(k)=f(xi);
    %display(o1(k));
    o2(k)=g(xi);
    %display(o2(k));
    if (k==201);
        display(k);
        return;
    end;
    xd(k+1)=xd(k)+d;
    c=[xd(k); o1(k); o2(k)];
    fprintf(results,'%6.20f %6.20f %6.20f\n',c);
    
end;
